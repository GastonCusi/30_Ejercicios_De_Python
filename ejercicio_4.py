#Declarando las variables
suma = 0.0
entero = 0

#Pidiendo el entero al usuario
print("Ingrese un entero positivo")
entero = int(input("> "))

#Haciendo la formula para hacer la suma de los primeros enteros positivos
suma = (entero * (entero + 1)) / 2

#Imprimiendo el resultado
print(f"La suma de los primeros enteros positivos es de: {suma}")